package model;

public class ParesImpares {

	private int mult, soma;

	public ParesImpares() {
	}

	public ParesImpares(int mult, int soma) {
		this.mult = mult;
		this.soma = soma;
	}

	public int getMult() {
		return mult;
	}

	public void setMult(int mult) {
		this.mult = mult;
	}

	public int getSoma() {
		return soma;
	}

	public void setSoma(int soma) {
		this.soma = soma;
	}
	
}